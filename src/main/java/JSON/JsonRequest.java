package JSON;

public class JsonRequest
{
    private String requestSender;
    private String requestDomain;
    private String requestDetails;
    private String reqeustTitle;

    public String getRequestSender()
    {
        return requestSender;
    }

    public void setRequestSender(String requestSender)
    {
        this.requestSender = requestSender;
    }

    public String getRequestDomain()
    {
        return requestDomain;
    }

    public void setRequestDomain(String requestDomain)
    {
        this.requestDomain = requestDomain;
    }

    public String getRequestDetails()
    {
        return requestDetails;
    }

    public void setRequestDetails(String requestDetails)
    {
        this.requestDetails = requestDetails;
    }

    public String getReqeustTitle()
    {
        return reqeustTitle;
    }

    public void setReqeustTitle(String reqeustTitle)
    {
        this.reqeustTitle = reqeustTitle;
    }
}
